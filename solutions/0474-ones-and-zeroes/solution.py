class Solution:
    def findMaxForm(self, strs: List[str], m: int, n: int) -> int:
        # dp[i][j] будет хранить размер самого большого подмножества
        # с использованием i нулей и j единиц.
        dp = [[0] * (n + 1) for _ in range(m + 1)]

        for s in strs:
            zeros = s.count('0')
            ones = s.count('1')

            # Итерируемся в обратном порядке, чтобы каждая строка использовалась не более одного раза.
            # Это ключевой аспект для задачи 0/1 рюкзака.
            for i in range(m, zeros - 1, -1):
                for j in range(n, ones - 1, -1):
                    # Для каждой ячейки dp[i][j] мы решаем:
                    # 1. Не брать текущую строку s -> значение остается dp[i][j]
                    # 2. Взять текущую строку s -> значение будет dp[i - zeros][j - ones] + 1
                    # Выбираем максимум из двух вариантов.
                    dp[i][j] = max(dp[i][j], dp[i - zeros][j - ones] + 1)

        # Конечный результат находится в dp[m][n]
        return dp[m][n]
